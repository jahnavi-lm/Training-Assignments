package AdvanceJavaJune05.AdvanceJavaAssignment_June04;

public class Student {
    private String name;
    private String gender;
    public Student(String name, String gender) {
        this.name = name;
        this.gender = gender;
    }
    public String getName() {
        return name;
    }
    public String getGender() {
        return gender;
    }

}
